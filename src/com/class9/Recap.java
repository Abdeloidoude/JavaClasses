package com.class9;

public class Recap {

	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
			for (int s = 0; s < 10; s++) {
				for (int t = 0; t < 10; t++) {
					for (int n = 0; n < 10; n++) {

						System.out.println(i + "" + s + "" + t + "" + n + "");
					}

				}
			}
		}
	}

}
